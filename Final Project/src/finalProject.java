
public class finalProject
	{
		private String suit;
		private String type;
		private int value;
		
		public finalProject(String s, String t, int v)
		{
			suit = s;
			type = t;
			value = v;
			
		}

		public String getSuit()
			{
				return suit;
			}

		public void setSuit(String suit)
			{
				this.suit = suit;
			}

		public String getType()
			{
				return type;
			}

		public void setType(String type)
			{
				this.type = type;
			}

		public int getValue()
			{
				return value;
			}

		public void setValue(int value)
			{
				this.value = value;
			}
			
		

		
	}
